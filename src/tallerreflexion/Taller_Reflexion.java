    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        clases.removeAllItems();
        atributos.removeAllItems();
        metodos.removeAllItems();

        String nombrePackete = txtPaquete.getText();

        ArrayList<Class<?>> classes = listarClases(nombrePackete);

        if (classes != null) {
            classes.forEach((clas) -> {
                clases.addItem(clas.getName());
            });
        }
    }//GEN-LAST:event_jButton1ActionPerformed

    private void clasesItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_clasesItemStateChanged
        if (evt.getStateChange() == ItemEvent.SELECTED) {

            atributos.removeAllItems();
            metodos.removeAllItems();

            try {
                clase = Class.forName(clases.getSelectedItem().toString());
                listaMetodos = clase.getMethods();
                listaAtributos = clase.getDeclaredFields();

                for (Method met : listaMetodos) {
                    metodos.addItem(met.getName());
                }

                for (Field fie : listaAtributos) {
                    atributos.addItem(fie.getName());
                }

            } catch (ClassNotFoundException c) {
            }
        }
    }//GEN-LAST:event_clasesItemStateChanged

